#include "../../includes/libft.h"

char	*ft_strdup(const char *s1)
{
	int		index;
	int		size;
	char	*s2;

	size = ft_strlen(s1);
	s2 = malloc (sizeof(char ) * (size + 1));
	index = 0;
	while (s1[index] != '\0')
	{
		s2[index] = s1[index];
		index++;
	}
	s2[index] = '\0';
	return (s2);
}
